//! Regression: `EdnCodec::decode` must terminate in bounded time on
//! adversarial inputs that drive mutual recursion through
//! `parse_vector` ↔ `parse_value` ↔ `parse_tagged`.
//!
//! ## Background
//!
//! The pre-fix EDN parser had no recursion cap on container parsers
//! (`parse_vector` / `parse_map` / `parse_set` / `parse_tagged`). A
//! crafted byte sequence such as repeated `[#X[#X[#X...` (with `X`
//! any non-whitespace stand-in including invalid-UTF-8 bytes mapped
//! to `U+FFFD` via `from_utf8_lossy`) drives:
//!
//! ```text
//! parse_vector('[') → parse_value('#X[#X…')
//!   → parse_tagged('#X[#X…')           // eats one chunk to next ws
//!     → parse_value('[#X…')            // recurse
//!       → parse_vector('[#X…')         // recurse
//!         → parse_value('#X…') …       // (loop)
//! ```
//!
//! Each frame re-walks the remaining input via `find(whitespace)` and
//! `trim_start`, and with U+FFFD multi-byte replacement bytes the
//! per-frame cost grows further. libFuzzer surfaced two seeds that
//! pinned `decode` for 50+ seconds (10 s timeout exceeded):
//!
//! - `fuzz/artifacts/codec_decode/timeout-cc6b8c35bcecbc7b92ac68c186623186d3c33612` (313-byte body)
//! - `fuzz/artifacts/codec_decode/timeout-45ca6cbbe01bc80e30213dbc27feac790f743586` (417-byte body)
//! - `fuzz/artifacts/codec_decode/timeout-946cc5ce4bd7dd243b502f97aeff502ac6dc7bdc` (313-byte body, surfaced by the post-depth-cap fuzz smoke)
//!
//! ## Fix
//!
//! Two-part change in `crates/ferro-stash-codec/src/edn.rs`:
//!
//! 1. **Depth cap.** Thread a `depth` counter through
//!    `parse_value_inner` / `parse_vector` / `parse_map` /
//!    `parse_set` / `parse_tagged`, returning `None` once depth
//!    reaches `MAX_DEPTH = 64` (sibling caps: ferrosearch `1af3262`
//!    parser recursion, ferrodruid `864f3ce` SQL parenthesis nesting).
//!    Container parsers additionally bail if a child parse fails to
//!    advance the cursor — defence-in-depth against future hot-loop
//!    regressions.
//!
//! 2. **Skip stringification of structural tagged values.** The
//!    pre-fix `parse_tagged` unknown-tag path serialized the inner
//!    value (which can be an `Array` or `Object`) via
//!    `serde_json::to_string` and wrapped the result in a
//!    `format!("#{tag} {}", …)`. Adversarial nesting drove that
//!    serialization at every recursion level — each outer call
//!    serialized a value that already contained the inner already-
//!    serialized strings, doubling the string size at each layer.
//!    Empirical observation: a 313-byte input grew the lossy buffer
//!    to **>1 GiB** before the stack unwound (`t_lossy=931ms` at
//!    depth 1). The fix passes structural values through unchanged
//!    for unknown tags (matching the existing `#inst`/`#uuid` fast
//!    path); scalars still preserve `#tag <scalar>` formatting.
//!
//! ## Assertion shape
//!
//! Both reproducers must complete a single `decode` call in well under
//! a second on a stock laptop CPU (post-fix observed: <1 ms in
//! release, <50 ms in debug). We allow generous slack (1 s) so the
//! test does not flake on contended CI hosts but still fails loudly
//! on the unbounded-recursion regression (which spends 10+ s).
//! Whether `decode` returns `Ok` or `Err` is not asserted — both are
//! acceptable; the contract is "no panic, no hang".

