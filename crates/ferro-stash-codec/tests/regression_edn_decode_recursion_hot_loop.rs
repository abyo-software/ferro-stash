/// Synthetic minimal trigger for the unknown-tag JSON
/// stringification blow-up. Each level wraps the previous in
/// `[#x ...]`, so an `Array` ends up as the value of an unknown
/// `#x` tag at every layer. Pre-fix this drove a string of size
/// `2^N` to be built; post-fix the value is passed through
/// unchanged, so total work is linear.
#[test]
fn edn_decode_unknown_tag_with_nested_array_terminates() {
    let mut data = Vec::with_capacity(8192);
    for _ in 0..32 {
        data.extend_from_slice(b"[#x ");
    }
    data.extend_from_slice(b"42");
    for _ in 0..32 {
        data.extend_from_slice(b"]");
    }
    assert_decode_terminates_quickly("nested unknown-tag array", &data);
}

/// Synthetic minimal trigger: `[#x` repeated to drive
/// `parse_vector → parse_value → parse_tagged → parse_value →
/// parse_vector → ...`. With the depth cap this aborts at 64 levels
/// and returns `Err` (or `Ok` of a partial structure if the parser
/// happened to commit). Without the cap this is at minimum O(n)
