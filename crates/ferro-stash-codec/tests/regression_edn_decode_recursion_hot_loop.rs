/// Generous timeout — post-fix the parsers complete in <1 ms
/// (release) / <50 ms (debug). Pre-fix they pinned for 50+ seconds.
/// 1 second leaves orders of magnitude headroom for slow CI yet
/// fails loudly on the recursion-cap regression.
const SAFETY_BUDGET: Duration = Duration::from_secs(1);

fn assert_decode_terminates_quickly(label: &str, body: &[u8]) {
    let codec = EdnCodec::default();
    let start = Instant::now();
    // Result is intentionally ignored — the decoder is allowed to
    // succeed (best-effort) or return `Err`. The bug under test is a
    // wall-clock hang.
    let _ = codec.decode(body);
    let elapsed = start.elapsed();
    assert!(
        elapsed < SAFETY_BUDGET,
        "{label}: EdnCodec::decode took {elapsed:?} (budget {SAFETY_BUDGET:?}) — \
         recursion cap regression?"
    );
}

#[test]
fn edn_decode_timeout_seed_cc6b8c35_terminates() {
    assert_decode_terminates_quickly("seed cc6b8c35", &TIMEOUT_BODY_1);
}

#[test]
fn edn_decode_timeout_seed_45ca6cbb_terminates() {
    assert_decode_terminates_quickly("seed 45ca6cbb", &TIMEOUT_BODY_2);
}

